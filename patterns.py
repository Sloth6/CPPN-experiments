import numpy as np

def pretty_print(x):
    """
    0 : Black
    1 : Green
    2 : Yellow
    3 : Red
    4 : Empty
    """
    # 91 = red
    colors = [ "\033[90m -", "\033[92m #", "\033[93m #", "\033[93m #", "\033[90m  "]
    fn = lambda i: colors[int(i)]
    for r in x:
        print(''.join(map(fn, r)))
    print("\033[00m")

